name = "Sakshi"

print(name[-4:-1]) #it will print ksh 
print(name[-5:-3]) # it will print ak
print(name[-6:-1]) #it will print Saksh

#syntax
'''
name[num1:num2]
num1 value is the value we want as the starting 
num2 value will be excluded but the numbers till the num2 will be print
'''

another_name = "Vaishnavi"
print(another_name[1:]) #it will print aishnavi
print(another_name[1:9]) #it will also print aishnavi and 9 is total length of the string

print(another_name[:4]) # it will start from 0 and print till 4 means "Vais"
print(another_name[0:4]) # it will start from 0 and print till 4 means "Vais"
